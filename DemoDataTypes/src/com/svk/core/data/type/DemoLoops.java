package com.svk.core.data.type;

public class DemoLoops {
	
public static void main(String[] args){
	
	int a = 21;
	
	while(a < 20){
		System.out.println(" Value of A " +a);
		a++;
	}

	int b = 21 ;
	
	do{
		System.out.println("Value of B :" +b);
		b++;
	}while(b <20);
	
	
	
	for(int c = 10 ; c <20 ; c++){
		System.out.println(" Value of C :" +c);
		
	}
	
	
}
}
