package com.svk.core.oops;

public class Address {
	
	private String city;
	private String state;
	
	//Encapsulation
	public String getCity(){
		return this.city;
	}
	
	//Encapsulation
	public String getState(){
		return this.state;
	}
	
	//Encapsulation
	public void setCity(String cityName){
		this.city = cityName;
	}
	
	//Encapsulation
	public void setState(String stateName){
		state = stateName;
	}


}
